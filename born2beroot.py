code = """0010 01 11 111 001 000||11 01 10||1 0000 01 1||
1010 111 11 0 000||011 00 1 0000||1 0000 0||
01 0100 1 0 010 10 01 1 0||0001 0 010 000 00 111 10||
111 0010||001100 001100 001100"""

dico = {
	"0010": 'f',
	"01": 'a',
	"11": 'm',
	"111": 'o',
	"001": 'u',
	'000': 's',
	'10': 'n',
	'1': 't',
	'0000': 'h',
	"0": 'e',
	'1010': 'c',
	'011': 'w',
	'00': 'i',
	'0100': 'l',
	'010': 'r',
	'0001': 'v',
	'001100': '?'
}

for word in code.replace('\n', '').strip().split('||'):
	word = word.strip()
	letters = word.split(' ')
	for i in letters:
		if i in dico.keys():
			print(dico[i], end = "")
		else:
			print(f"/{i}/", end = "")
	print(" ", end = "")

print()
